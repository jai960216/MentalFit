      debugPrint('Ï¥àÍ∏∞ Îç∞Ïù¥ÌÑ∞ Î°úÎî© Ïò§Î•ò: $e');
    }
  }

  @override
  void dispose() {
    _greetingController.dispose();
    _cardController.dispose();
    super.dispose();
  }

